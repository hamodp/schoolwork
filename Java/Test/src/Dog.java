
public class Dog 
{
	private int dogage;
	private String title;
	public Dog (String name, int age)
	//constructor
	{
		age = dogage;
		name = title;
	}
	public int getDogage(int age)
	{
		dogage = age;
		return dogage;
	}
	public int setHumanage(int age)
	{
		int humanage = age *7;
		return humanage;
	}
}
