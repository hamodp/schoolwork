
public class average_integer
//practice with integers and their average
{
	public static void main ( String [] arg)
	{
		int constant1= 2;
		int constant2= 4;
		int constant3= 6;
		// uses three constants to calculate average
		double average= (constant1 + constant2 + constant3)/3.0;
		System.out.println(constant1);
		System.out.println(constant2);
		System.out.println(constant3);
		System.out.println("average:" + average);
	}
}
